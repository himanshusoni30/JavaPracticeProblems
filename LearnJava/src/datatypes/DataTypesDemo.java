package datatypes;

public class DataTypesDemo {

	public static void main(String[] args) {
		byte a = -50;
		short b = 150;
		int c = 10000;
		long d = 1000000;
		char e = 'E';
		float f = 1.23f; //f is used to differentiate between double and float
		double g = 12345.67890;
		byte h = 20;
		byte result = (byte) (a+h); //it always return integer value when two bytes are added
	}
}
